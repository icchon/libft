#include "libft.h"

static int	ft_islower(int c)
{
	return ('a' <= c && c <= 'z');
}

int	ft_toupper(int c)
{
	if (ft_islower(c))
	{
		return (c + ('A' - 'a'));
	}
	return (c);
}
